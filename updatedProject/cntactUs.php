<p><strong>HISTORY OF THE INSTITUTE<br></strong> 
The National Institute of Technology (formerly known as Regional Engineering College)
Tiruchirappalli, situated in the heart of Tamil Nadu on the banks of river Cauvery,
was started as a joint and co-operative venture of the Government of India and the
Government of Tamil Nadu in 1964 with a view to catering to the needs of man-power
in technology for the country. The college has been conferred with autonomy in financial
and administrative matters to achieve rapid development. Because of this rich experience,
this institution was granted Deemed University Status with the approval of the UGC/AICTE
and Govt. of India in the year 2003 and renamed as National Institute of Technology.
NIT-T was registered under Societies Registration Act XXVII of 1975.

</p>
<p>&nbsp;</p>
<p>The Chairman of NITT is Dr. Rajaram Nityananda (also Senior Professor, National Centre for Radio Astrophysics, Tata Institute of Fundamental Research) and the Director of NITT is Dr.Srinivasan Sundarrajan

 </p>
<p>&nbsp;</p>
<p>The institution offers Under Graduate Courses in ten branches and Post Graduate Courses in twenty one disciplines of Science, Engineering & Technology besides M.S. (by Research) and Ph.D. in all the departments. The faculty is inducted through a process of open advertisement throughout the country. The institute is an example of cultural unity with students drawn from most of the states in the country.

The hallmark of the campus is the good facilities which caters to the academic and extra curricular interests of the students. The Octagon is the pride of the campus equipped with modern facilities like a CAD/CAM Lab, Local Area Network, High Speed Internet connection and other seminar and conference facilites. It is maintained and run by the Computer Support Group (CSG) of the institute.

Apart from this the campus provides ample opportunities for developing extra curricular skills which include NCC, NSS, Students Chapters of IEEE, social clubs and sports & games. The Alumni of this institution have excelled in various spheres and are positioned very well globally in a number of leading Government, Public Sector & Private Organizations.

NIT-T hosts two inter collegiate fests namely Festember (Cultural) and Pragyan (Techinical) and an inter department fest namely NITTfest (Cultural) annually. These fests draw students from most colleges of South India and are hugely popular. Apart from this each department conducts Symposium.

The College has a total campus area of 800 acres. This includes good hostel facilites, Hospital, Post & Telegraph, Telecom Center, fully computerized State Bank of India (SBI) NIT branch with ATM facility, Book stall, Reprographic Center, Canteen, Swimming pool and Co-op. Stores.
</p>

